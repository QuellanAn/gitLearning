package com.catic.mobilehos.pay.entity;

public class PayOvertime {

	private String overTimeId;
	private String outTradeNo;
	private String transactionId;
	private String createTime;
	private Integer amount;
	private Integer payStatus;
	private Integer refundStatus;
	private Double D_amount;
	
	
	
	
	public String getOverTimeId() {
		return overTimeId;
	}
	public void setOverTimeId(String overTimeId) {
		this.overTimeId = overTimeId;
	}
	public String getOutTradeNo() {
		return outTradeNo;
	}
	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}
	public String getTransactionId() {
		return transactionId;
	}
	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public Integer getAmount() {
		return amount;
	}
	public void setAmount(Integer amount) {
		this.amount = amount;
	}
	public Integer getPayStatus() {
		return payStatus;
	}
	public void setPayStatus(Integer payStatus) {
		this.payStatus = payStatus;
	}
	public Integer getRefundStatus() {
		return refundStatus;
	}
	public void setRefundStatus(Integer refundStatus) {
		this.refundStatus = refundStatus;
	}
	public Double getD_amount() {
		return D_amount;
	}
	public void setD_amount(Double d_amount) {
		D_amount = d_amount;
	}

}
