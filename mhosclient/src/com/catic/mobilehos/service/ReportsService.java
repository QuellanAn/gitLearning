package com.catic.mobilehos.service;

/**
 * 查看报告功能相关的服务
 * @author xieweipeng
 *
 */
public interface ReportsService {
	
	/**
	 * 获得用户的所有检查检验报告
	 * @param userId
	 * @return
	 */
	ServiceResult getAllReportsListByUserId(String userId);
	
	/**
	 * 获得用户最近一月的检查报告
	 * @param userId
	 * @return
	 */
	ServiceResult getMonthReportsListByUserId(String userId);
	
	
	/**
	 * 获得检查报告的明细信息
	 * @param reportId
	 * @return
	 */
	ServiceResult getChkReportDetail(String reportId);
	
	
	/**
	 * 获得检验报告的明细信息
	 * @param reportId
	 * @return
	 */
	ServiceResult getExamReportDetails(String reportId);

}
