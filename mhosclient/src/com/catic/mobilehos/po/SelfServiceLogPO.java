package com.catic.mobilehos.po;  

import java.sql.Timestamp;

/**  
 * 类说明:自助终端日志
 * @author 朱伟权
 * 创建时间: 2017-6-27 上午10:35:55
 */
public class SelfServiceLogPO {
	
	private String id;
	private String logType;// 日志类型，0为系统日志，1为交易日志，2为异常日志
	private String logStatus;// 日志状态，默认0正常
	private String facilityId;// 终端编号
	private String logFileName;// 日志名称
	private String logPath;// 日志路径
	private Timestamp updateTime;// 更新时间
	
	private String district;// 院区
	private String facilityType;// 设备类型
	private String putAddress;// 位置信息
	private String beginDate;// 查询开始时间
	private String endDate;// 查询结束时间
	private Integer count;// 统计
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLogType() {
		return logType;
	}
	public void setLogType(String logType) {
		this.logType = logType;
	}
	public String getLogStatus() {
		return logStatus;
	}
	public void setLogStatus(String logStatus) {
		this.logStatus = logStatus;
	}
	public String getFacilityId() {
		return facilityId;
	}
	public void setFacilityId(String facilityId) {
		this.facilityId = facilityId;
	}
	public String getLogFileName() {
		return logFileName;
	}
	public void setLogFileName(String logFileName) {
		this.logFileName = logFileName;
	}
	public String getLogPath() {
		return logPath;
	}
	public void setLogPath(String logPath) {
		this.logPath = logPath;
	}
	public Timestamp getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Timestamp updateTime) {
		this.updateTime = updateTime;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getFacilityType() {
		return facilityType;
	}
	public void setFacilityType(String facilityType) {
		this.facilityType = facilityType;
	}
	public String getBeginDate() {
		return beginDate;
	}
	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getPutAddress() {
		return putAddress;
	}
	public void setPutAddress(String putAddress) {
		this.putAddress = putAddress;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}

}
 