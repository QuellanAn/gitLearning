package com.catic.mobilehos.po;  

import java.sql.Timestamp;

/**  
 * 类说明:版本升级历史记录
 * @author 朱伟权
 * 创建时间: 2017-7-19 下午6:16:04
 */
public class SelfserviceUpgradeHistoryPO {
	
	private String id;
	private String facilityId;// 终端编号
	private String versionId;// 版本记录id
	private Timestamp upgrade_time;// 升级时间
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFacilityId() {
		return facilityId;
	}
	public void setFacilityId(String facilityId) {
		this.facilityId = facilityId;
	}
	public String getVersionId() {
		return versionId;
	}
	public void setVersionId(String versionId) {
		this.versionId = versionId;
	}
	public Timestamp getUpgrade_time() {
		return upgrade_time;
	}
	public void setUpgrade_time(Timestamp upgrade_time) {
		this.upgrade_time = upgrade_time;
	}

}
 